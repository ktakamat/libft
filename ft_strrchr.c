#include "libft.h"

char    *ft_strrchr(const char *s,int c)
{
    char    *a;

    a = (char *)s + ft_strlen(s);
    while (*a != c)
    {
        if (a == s)
        {
            return (NULL);
        }
        a--;
    }
    return (a);
}
